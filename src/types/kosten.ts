class Kosten {
    holz: number;
    gold: number;
    stein: number;
    magie: number;

    constructor(holz: number, gold: number, stein: number, magie: number) {
        this.holz = holz;
        this.gold = gold;
        this.stein = stein;
        this.magie = magie;
    }
}

export default Kosten;